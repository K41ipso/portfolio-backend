class ChatRequest(BaseModel):
    message: str

class ChatResponse(BaseModel):
    response: str

def find_best_response(message: str) -> str:
    message_lower = message.lower()
    
    best_match = None
    best_score = 0
    
    for item in KNOWLEDGE_BASE:
        score = 0
        for keyword in item["keywords"]:
            if keyword in message_lower:
                score += len(keyword)  # длинные совпадения важнее
            elif len(keyword) > 3 and keyword[:-1] in message_lower:
                score += len(keyword) * 0.5
        
        if score > best_score:
            best_score = score
            best_match = item
    
    if best_match and best_score > 0:
        return random.choice(best_match["responses"])
    
    return "Интересный вопрос! К сожалению, у меня нет точной информации об этом в моей базе. Рекомендую связаться с Георгием напрямую через Telegram @gpopovdev или Email karandawwik@gmail.com — он ответит на все вопросы детально и полно!"

@app.post("/api/chat")
async def chat(request: ChatRequest):
    try:
        # Имитация "раздумья" — задержка от 1 до 3 секунд
        await asyncio.sleep(random.uniform(1, 3))
        
        response = find_best_response(request.message)
        return ChatResponse(response=response)
    except Exception as e:
        print(f"Error: {e}")
        return ChatResponse(
            response="Извини, произошла техническая ошибка. Попробуй ещё раз или свяжись со мной напрямую через Telegram @gpopovdev"
        )

